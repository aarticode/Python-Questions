#Arithmetic operators perform basic arithmetic operations.
# For example "+" is used to add and "?" is used for subtraction.

# Adding two values
print(12+23)
# Subtracting two values
print(12-23)
# Multiplying two values
print(12*23)
# Dividing two values
print(12/23)

#Relational Operators are used to comparing the values.
# These operators test the conditions and then returns a boolean value either True or False.

# Examples of Relational Operators
a, b = 10, 12
print(a==b) # False
print(a<b) # True
print(a<=b) # True
print(a!=b) # True